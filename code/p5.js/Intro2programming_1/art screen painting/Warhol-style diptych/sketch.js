var cir = [
  [
    [25, 25, 112],
    [148, 0, 211],
    [139, 69, 19],
  ],
  [
    [244, 164, 96],
    [188, 143, 143],
    [176, 196, 222],
  ],
  [
    [0, 255, 255],
    [250, 128, 114],
    [0, 128, 128],
  ],
];

function setup() {
  createCanvas(601, 601);
}

function draw() {
  for (i = 0; i < 3; i++) {
    for (j = 0; j < 3; j++) {
      noStroke();
      fill(200, i * 100, j * 80 + 60);
      rect(i * 200, j * 200, 200, 200);
      fill(cir[i][j]);
      //   fill((i + j) * 80, j * 80 + 75, i * 100 + 55);
      ellipse(i * 200 + 100, j * 200 + 100, 180, 180);
      fill(0);
      ellipse(i * 200 + 100 - 40, j * 200 + 100, 20, 20);
      ellipse(i * 200 + 100 + 40, j * 200 + 100, 20, 20);
    }
  }
}
